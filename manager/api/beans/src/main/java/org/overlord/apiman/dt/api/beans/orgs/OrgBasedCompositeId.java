package org.overlord.apiman.dt.api.beans.orgs;

import java.io.Serializable;

/**
 * Composite key for entities that are owned by an organization.
 *
 * @author eric.wittmann@redhat.com
 */
public class OrgBasedCompositeId implements Serializable {
    
    private static final long serialVersionUID = 1908517963509334989L;
    
    private String organizationId;
    private String id;
    
    /**
     * Constructor.
     */
    public OrgBasedCompositeId() {
    }

    /**
     * Constructor.
     * @param organizationId
     * @param id
     */
    public OrgBasedCompositeId(String organizationId, String id) {
        this.setOrganizationId(organizationId);
        this.setId(id);
    }

    /**
     * @return the organizationId
     */
    public String getOrganizationId() {
        return organizationId;
    }

    /**
     * @param organizationId the organizationId to set
     */
    public void setOrganizationId(String organizationId) {
        this.organizationId = organizationId;
    }

    /**
     * @return the id
     */
    public String getId() {
        return id;
    }

    /**
     * @param id the id to set
     */
    public void setId(String id) {
        this.id = id;
    }

    /**
     * @see java.lang.Object#hashCode()
     */
    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + ((id == null) ? 0 : id.hashCode());
        result = prime * result + ((organizationId == null) ? 0 : organizationId.hashCode());
        return result;
    }

    /**
     * @see java.lang.Object#equals(java.lang.Object)
     */
    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        OrgBasedCompositeId other = (OrgBasedCompositeId) obj;
        if (id == null) {
            if (other.id != null)
                return false;
        } else if (!id.equals(other.id))
            return false;
        if (organizationId == null) {
            if (other.organizationId != null)
                return false;
        } else if (!organizationId.equals(other.organizationId))
            return false;
        return true;
    }

}
